import Foundation

// Response for like/unlike actions
struct LikeResponse: Codable {
    let postId: Int
    let isLiked: Bool
    let likesCount: Int
}

// Response for follow/unfollow actions
struct FollowResponse: Codable {
    let userId: Int
    let isFollowing: Bool
    let followersCount: Int
}
